#include <unistd.h>

void	ft_print_comb(void)
{
	char buf[3];

	buf[0] = '0';
	buf[1] = '1';
	buf[2] = '2';
	while (buf[0] <= '7')
	{
		while (buf[1] <= '8')
		{
			while (buf[2] <= '9')
			{
				if ((buf[0] == '7') && (buf[1] == '8') && (buf[2] == '9'))
					break ;
				write(1, buf, 3);
				write(1, ", ", 2);
				buf[2]++;
			}
			buf[1]++;
			buf[2] = buf[1] + 1;
		}
		buf[0]++;
		buf[1] = buf[0] + 1;
		buf[2] = buf[1] + 1;
	}
	write(1, "789", 3);
}
